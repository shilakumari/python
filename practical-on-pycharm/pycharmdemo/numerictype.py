#int type
a=13
b=100
c=-66
print(a,b,c)
print(type(c))

#float type
x=20.9
y=13.6
z=203.0
print(x,y,z)
print(type(z))

#complex type
d=2+3j
print(type(d))

#Binary type(start with 0B or 0b)
e=0B01010
print(e)
print(type(e))

#Hexadecimal type(start with 0X or 0x)
f=0xFF
print(f)
print(type(f))

#Octal type(start with 0o)
g=0o45
print(g)
print(type(g))

#boolean
h=True
print(type(h))
print(6>9)

#Type conversion
i=int(y)
print(i)
print(type(i))

j=float("22.6")
print(j)
print(type(j))

k=hex(10)
#l=oct(10)
#m=bin(10)
